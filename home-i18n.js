const LANG_NAMES = {
  es:'Español', en:'English', fr:'Français', de:'Deutsch', pt:'Português',
  it:'Italiano', ru:'Русский', bg:'Български', zh:'中文', ja:'日本語',
  ko:'한국어', ar:'العربية', hi:'हिन्दी', tr:'Türkçe', nl:'Nederlands',
  sv:'Svenska', pl:'Polski', ro:'Română', el:'Ελληνικά', th:'ไทย',
  vi:'Tiếng Việt', id:'Bahasa', he:'עברית', uk:'Українська', cs:'Čeština'
};

let homeLang = 'es';

function detectHomeLang() {
  const stored = localStorage.getItem('aaLang');
  if (stored && HOME_T[stored]) { homeLang = stored; return; }
  const nav = (navigator.language || navigator.userLanguage || 'es').toLowerCase();
  const code = nav.split('-')[0];
  if (HOME_T[code]) { homeLang = code; } else { homeLang = 'es'; }
}

function ht(key) {
  return (HOME_T[homeLang] && HOME_T[homeLang][key]) || HOME_T.es[key] || key;
}

function applyHomeLang() {
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.getAttribute('data-t');
    const val = ht(key);
    if (val) el.innerHTML = val;
  });
  document.documentElement.lang = homeLang;
  // RTL support
  if (homeLang === 'ar' || homeLang === 'he') {
    document.documentElement.dir = 'rtl';
  } else {
    document.documentElement.dir = 'ltr';
  }
  // Update selector
  const sel = document.getElementById('lang-select');
  if (sel) sel.value = homeLang;
}

function changeHomeLang(code) {
  if (!HOME_T[code]) return;
  homeLang = code;
  localStorage.setItem('aaLang', code);
  applyHomeLang();
}

// Build language selector and init on DOM ready
document.addEventListener('DOMContentLoaded', function() {
  // Inject language selector into nav
  const nav = document.querySelector('nav');
  if (nav) {
    const sel = document.createElement('select');
    sel.id = 'lang-select';
    sel.style.cssText = 'background:rgba(255,237,138,0.1);border:1px solid rgba(255,237,138,0.3);color:#FFED8A;padding:6px 10px;border-radius:8px;font-size:0.8rem;font-family:Inter,sans-serif;cursor:pointer;outline:none;';
    Object.keys(LANG_NAMES).forEach(code => {
      if (HOME_T[code]) {
        const opt = document.createElement('option');
        opt.value = code;
        opt.textContent = LANG_NAMES[code];
        opt.style.cssText = 'background:#08011B;color:#fff;';
        sel.appendChild(opt);
      }
    });
    sel.addEventListener('change', function() { changeHomeLang(this.value); });
    nav.appendChild(sel);
  }

  detectHomeLang();
  applyHomeLang();
});
